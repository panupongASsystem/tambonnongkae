                            // Fallback: ‡∏™‡∏£‡πâ‡∏≤‡∏á instance ‡πÉ‡∏´‡∏°‡πà‡πÅ‡∏•‡πâ‡∏ß‡∏õ‡∏¥‡∏î
                            const newModalInstance = new bootstrap.Modal(modalElement);
